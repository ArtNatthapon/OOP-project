    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    // TODO add your handling code here:
       try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/oopproject","root","");
           String sql = "Select * from user where username=? and password= ?";
           PreparedStatement pst = con.prepareStatement(sql);
           pst.setString(1, Username.getText());
           pst.setString(2, Password.getText());
           ResultSet rs = pst.executeQuery();
           if(rs.next()){
               
               //JOptionPane.showMessageDialog(null, "Username and Password Matched");
               howtoPage mnu = new howtoPage();
               mnu.setVisible(true);
               mnu.pack();
               mnu.setLocationRelativeTo(null);
               mnu.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
               this.dispose();
           }
           else{
               JOptionPane.showMessageDialog(null, "SIGN IN below!!");
               Username.setText("");
               Password.setText("");
           }
           con.close();
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

